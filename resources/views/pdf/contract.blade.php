        </li>
        <li>
            <span>پذیرنده با اطلاع از ضوابط شبکه پرداخت، اعلام رضایت نمود، شارژ¬پل مجاز خواهد بود در صورت مشکوک بودن عملکرد پذیرنده و یا دریافت هرگونه شکایتی از سوی مشتریان ایشان وجوه مورد مناقشه را تا زمان بر طرف شدن شبهات مسدود نماید.</span></br>
        </li>
        <li>
            <span>شارژ¬پل می تواند، درصورت تشخیص پذیرنده به عنوان پذیرنده خاطی وی را به نهاد¬های قانونی معرفی و نسبت به قطع خدمات پرداخت الکترونیک به صورت یک¬طرفه و ثبت سوابق ایشان در پایگاه داده شرکت شاپرک اقدام نماید. دریافت خدمات از سایر شرکتهای P.S.P منوط به تسویه حساب کامل قرارداد و تائید شرکت شاپرک در این خصوص می باشد. </span></br>
        </li>
        <li>
            <span>
