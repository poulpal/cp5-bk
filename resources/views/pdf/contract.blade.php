            <span>شارژپل مجاز است در صورت عدم رعایت مفاد این قرارداد توسط مدیرمسئول و یا درصورت عدم کارآئی مناسب در هر زمان که مصلحت بداند مراتب را به مدیرمسئول اعلام و حداکثر ظرف مدت سه ماه از تاریخ اعلام نسبت به تسویه حساب با مدیرمسئول اقدام نماید.</span></br>
        </li>
    </ol>


        <span>ماده 10) سایر شرایط</span></br>

    <ol>
        <li>
